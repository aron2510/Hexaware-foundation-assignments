class User:
    def __init__(self, user_id=None, username=None, password=None, email=None, first_name=None, last_name=None, dob=None, profile_picture=None):
        self.user_id = user_id
        self.username = username
        self.password = password
        self.email = email
        self.first_name = first_name
        self.last_name = last_name
        self.dob = dob
        self.profile_picture = profile_picture
